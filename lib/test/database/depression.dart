  static ResultModel calculateResult(int score) {
    if (score.clamp(0, 4) == score) {
      return Depression.result[0];
    } else if (score.clamp(5, 9) == score) {
      return Depression.result[1];
    } else if (score.clamp(10, 14) == score) {
      return Depression.result[2];
    } else if (score.clamp(15, 19) == score) {
      return Depression.result[3];
    } else {
      return Depression.result[4];
    }
  }


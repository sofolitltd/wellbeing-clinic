  static ResultModel calculateResult(int score) {
    if (score.clamp(0, 3) == score) {
      return Hopelessness.result[0];
    } else if (score.clamp(4, 8) == score) {
      return Hopelessness.result[1];
    } else if (score.clamp(9, 14) == score) {
      return Hopelessness.result[1];
    } else if (15 <= score) {
      return Hopelessness.result[1];
    } else {
      return ResultModel.empty();
    }
  }

